namespace Microsoft.Windows.Controls.DataVisualization.Charting
{
    /// <summary>
    /// The location of the axis.
    /// </summary>
    public enum AxisLocation
    {
        /// <summary>
        /// Automatically determine where to place the axis.
        /// </summary>
        Auto = 0,

        /// <summary>
        /// Place axis on the left.
        /// </summary>
        Left = 1,

        /// <summary>
        /// Place axis on the bottom.
        /// </summary>
        Bottom = 2,

        /// <summary>
        /// Place axis on the right.
        /// </summary>
        Right = 3,

        /// <summary>
        /// Place axis on the top.
        /// </summary>
        Top = 4
    }
}